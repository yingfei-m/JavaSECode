package com.base.learn;

/**
 * 获取两个数中的较大值
 *
 * @Author myf15609
 * @Date 2025/1/12
 */
public class ConditionExer1 {
    public static void main(String[] args) {
        int m1 = 20;
        int m2 = 30;
        int max1 = (m1 > m2) ? m1 : m2;
        System.out.println("m1和m2种较大值为：" + max1);
    }
}
