package com.base.learn;

/**
 * 定义一个整数，判定是偶数还是奇数
 *
 * @Author myf15609
 * @Date 2025/1/12
 */
public class IfElesTest2 {
    public static void main(String[] args) {
        int a = 11;
        if (a % 2 == 0){
            System.out.println(a + "是偶数");
        } else {
            System.out.println(a + "是奇数");
        }
    }
}
